setPushables({
  [p1]: []
})

// P1 inputs
onInput("d", () => {
  getFirst(p1).x += 1
  if (p1_x != 13) {
    p1_x += 1
  }
});
onInput("a", () => {
  getFirst(p1).x -= 1
  if (p1_x != 0) {
    p1_x -= 1
  }
});
onInput("w", () => {
  clearTile(p1_x, p1_y);
  addSprite(p1_x, p1_y, "f");
  setTimeout(() => {
    clearTile(p1_x, p1_y);
    addSprite(p1_x, p1_y, "d");
  }, 200);
});

// P2 inputs
onInput("j", () => {
  getFirst(p2).x -= 1
})
onInput("l", () => {
  getFirst(p2).x += 1
})

